# LIST STORE DIFFERENT TYPE OF DATA
'''
integer data --> 2314
float data --> 3.30
string data --> Anisul Alam
Boolean data --> True/False
'''
My_self =["Anisul Alam",2314,3.30,True]
print(My_self,type(My_self))
i=0
while i<=len(My_self)-1:
    print(My_self[i],type(My_self[i]))
    i += 1
