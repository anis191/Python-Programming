# Empty/Null tuple:-
# Syntax --> tuple_name = ()
info = ()
list=[1,2,3,4,5]
info = (list,'anis')
print(info)
# 1 value tuple:-
# Syntax --> tuple_name = (item1,)
tuple1 = ('anis')
print("tuple1 data type: ",type(tuple1))
tuple2 = ('anis',) # if just 1 value have a tuple, it should need to write this way!
print("tuple2 data type: ",type(tuple2))