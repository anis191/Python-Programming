# sort(): The sort() method sorts the list ascending order(number wais and alphabetically) by default.
# Syntax --> list.sort()
num = [30,10,50,20,40]
num.sort()
print(num)
char = ['g','i','a','k','q','b','t','g','a']
char.sort()
print(char)
name = ['Anis','Muhi','shariar','Rahim','karim']
name.sort()
print(name)
# sort(reverse=True): This method sorts the list descending order(number wais and alphabetically)
# Syntax --> list.sort(reverse=True)
num.sort(reverse=True)
print(num)
char.sort(reverse=True)
print(char)
name.sort(reverse=True)
print(name)