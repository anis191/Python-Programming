# remove(): This method Removes a specific item from list when it first time found
# Syntax --> list.remove(item)
num = [30,5,6,7,9]
num.remove(6)
print(num)
name = ['anis','muhi','shariar','robi']
name.remove('robi')
print(name)

# pop(): Removes a element at the specified position(remove a item using it's index number)
# Syntax --> list.pop(index_no)
a = [5,6,7,0]
a.pop(1)
print(a)
b=['banana','mango','pinapple']
b.pop(2)
print(b)

# reverse(): This method Reverses a list by order(index wais).It reverse the whole list
# Syntax --> list.reverse()
x = [17,22,33,0,1,4]
x.reverse()
print(x)
y = ['abc','def','ghi','jkl']
y.reverse()
print(y)